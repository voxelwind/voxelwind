package com.voxelwind.api.game.util.data;

/**
 * Specifies what direction logs should be in.
 */
public enum LogDirection {
    /**
     * This log is vertical (in the Y direction).
     */
    VERTICAL,
    /**
     * This log is horizontal (pointing in the X direction).
     */
    HORIZONTAL_X,
    /**
     * This log is horizontal (pointing in the Z direction).
     */
    HORIZONTAL_Z,
    /**
     * This log has no direction.
     */
    NONE
}
